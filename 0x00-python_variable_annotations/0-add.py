#!/usr/bin/env python3

"""
This module provides a function to add two floats.
"""
def add(a:float, b:float) -> float:
    """
    Adds two floats.

    Args:
        a (float): The first float.
        b (float): The second float.

    Returns:
        float: The sum of a and b.
    """
    return a + b

